-- ==========================================================
-- 1. Total Records
-- ==========================================================

SELECT COUNT(*) AS total_records
FROM superstore;

-- ==========================================================
-- 2. View First 10 Records
-- ==========================================================

SELECT *
FROM superstore
LIMIT 10;

-- ==========================================================
-- 3. View Table Structure
-- ==========================================================

PRAGMA table_info(superstore);

-- ==========================================================
-- 4. Date Range
-- ==========================================================

SELECT
MIN("Order Date") AS first_order,
MAX("Order Date") AS last_order
FROM superstore;

-- ==========================================================
-- 5. Total Sales
-- ==========================================================

SELECT
ROUND(SUM(Sales),2) AS total_sales
FROM superstore;

-- ==========================================================
-- 6. Total Profit
-- ==========================================================

SELECT
ROUND(SUM(Profit),2) AS total_profit
FROM superstore;

-- ==========================================================
-- 7. Total Quantity Sold
-- ==========================================================

SELECT
SUM(Quantity) AS total_quantity
FROM superstore;

-- ==========================================================
-- 8. Total Orders
-- ==========================================================

SELECT
COUNT(DISTINCT "Order ID") AS total_orders
FROM superstore;

-- ==========================================================
-- 9. Total Customers
-- ==========================================================

SELECT
COUNT(DISTINCT "Customer ID") AS total_customers
FROM superstore;

-- ==========================================================
-- 10. Regions
-- ==========================================================

SELECT DISTINCT Region
FROM superstore;

-- ==========================================================
-- 11. Categories
-- ==========================================================

SELECT DISTINCT Category
FROM superstore;

-- ==========================================================
-- 12. Sub-Categories
-- ==========================================================

SELECT DISTINCT "Sub-Category"
FROM superstore
ORDER BY "Sub-Category";

-- ==========================================================
-- 13. Ship Modes
-- ==========================================================

SELECT DISTINCT "Ship Mode"
FROM superstore;

-- ==========================================================
-- 14. Segments
-- ==========================================================

SELECT DISTINCT Segment
FROM superstore;

-- ==========================================================
-- 15. States
-- ==========================================================

SELECT
COUNT(DISTINCT State) AS total_states
FROM superstore;

-- ==========================================================
-- 16. Cities
-- ==========================================================

SELECT
COUNT(DISTINCT City) AS total_cities
FROM superstore;

-- ==========================================================
-- 17. Products
-- ==========================================================

SELECT
COUNT(DISTINCT "Product ID") AS total_products
FROM superstore;

-- ==========================================================
-- 18. Average Sales
-- ==========================================================

SELECT
ROUND(AVG(Sales),2) AS average_sales
FROM superstore;

-- ==========================================================
-- 19. Average Profit
-- ==========================================================

SELECT
ROUND(AVG(Profit),2) AS average_profit
FROM superstore;

-- ==========================================================
-- 20. Highest Sale
-- ==========================================================

SELECT
MAX(Sales) AS highest_sale
FROM superstore;

